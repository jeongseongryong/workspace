/* 관계연산자 - ==, !=, <, <=, >, >=
 * 
 */
package step03;

public class Test03_1 {
  public static void main(String[] args) {
  	System.out.println(5 == 3);
  	System.out.println(5 != 3);
  	System.out.println(5 < 3);
  	System.out.println(5 <= 3);
  	System.out.println(5 > 3);
  	System.out.println(5 >= 3);
  	
  }
}